package oops.Inheritance;

public class Calculator {

	void addition(int a, int b) {
		System.out.println(a + b);
	}

	void subtraction(int a, int b) {
		System.out.println(a - b);
	}

	void multipilication(int a, int b) {
		System.out.println(a * b);
	}

	void division(int a, int b) {
		System.out.println(a / b);
	}

}
